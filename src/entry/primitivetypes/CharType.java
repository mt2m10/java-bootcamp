package entry.primitivetypes;

public class CharType {
    public static void main(String[] args) throws Exception {

        char alphabet = 'E';
        char number = '9';
        char symbol = '%';
        char japaneseChar = 'あ';

        System.out.printf("英字 = %s%n", alphabet);
        System.out.printf("数字 = %s%n", number);
        System.out.printf("記号 = %s%n", symbol);
        System.out.printf("日本語 = %s%n", japaneseChar);
    }
}
